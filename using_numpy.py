import numpy as np

x = 1.0 #float x = 1
y = 2.0 # another float y = 2

#trig!
print(np.sin(x))
print(np.cos(x))
print(np.tan(x))
print(np.arcsin(x))
print(np.arccos(x))
print(np.arctan(x))
print(np.arctan2(x,y))	#arctan(x/y)

#hyperbolic functions
print(np.sinh(x))	#sinh(x)
print(np.cosh(x))	#cosh(x)
print(np.tanh(x))	#tanh(x)
print(np.arcsinh(x))
print(np.arccosh(x))
print(np.arctanh(x))
